const deleteUserPhotoServer = async photo => {
  if (photo.startsWith('default')) return;

  const path = `${__dirname}/../public/img/users/${photo}`;
  const unlink = util.promisify(fs.unlink);

  try {
    await unlink(path);
  } catch (error) {
    console.log(error);
  }
}; //C2 de xoa anh

